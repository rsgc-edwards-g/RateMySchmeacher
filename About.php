    <h2>Shmee is a web app that allows students to provide quick and meaningful 
    feedback to their teachers after each given class. We provide an easy to use
    interface for both students and teachers to rate and view ratings respectively.
    Students have access to their schedules and the option to rate each course in their
    schedule based on the last class taken in the course. Teachers can view their own 
    schedules of classes they are teaching and various graphs of recent average ratings
    in each of 3 catagories: Understanding, Engagement, and Productive. Teachers are also 
    able to edit the participants in their classes in the case of a drop in or a drop out.
    Teachers can also abuse this ability to trim the kids who rate their class poorly, thus
    boosting their ratings. We hope the Shmee will provide students with the power to shape
    the discussion in their classroom</h2>
    
